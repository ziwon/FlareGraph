// Bundles migrations/*.sql into migrations/index.js so both the CLI (Node)
// and non-filesystem consumers can apply them without reading files at runtime.
import { readdirSync, readFileSync, writeFileSync } from 'node:fs';
import { fileURLToPath } from 'node:url';
import { dirname, join } from 'node:path';

const dir = join(dirname(fileURLToPath(import.meta.url)), '..', 'migrations');
const files = readdirSync(dir)
  .filter((f) => f.endsWith('.sql'))
  .sort();

const entries = files.map((f) => ({ name: f, sql: readFileSync(join(dir, f), 'utf8') }));
const js = `// Generated by scripts/bundle-migrations.mjs — do not edit.
export const migrations = ${JSON.stringify(entries, null, 2)};
`;
writeFileSync(join(dir, 'index.js'), js);
writeFileSync(
  join(dir, 'index.d.ts'),
  'export declare const migrations: { name: string; sql: string }[];\n',
);
console.log(`bundled ${files.length} migration(s)`);
